# Project's name
Angry Biker

## Description
The player is an urban cyclist that hates turists. The ciclist must crash as many turists as posible
to win points. Avoid crashing against cars.



## MVP (DOM - CANVAS)
MVP definition, deliverables.

- 2D canvas context with a road and sidewalk background the move.
- Bike is static at the left end of the screen but the player is able to move the bike up and down, giving
  the impression the bike changes lanes on the road. 
- Random turists appear on the road.
- Collision between turist and bike give points to the player.


